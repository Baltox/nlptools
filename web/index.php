<p>Text processing framework to analyse Natural Language by performing
operations and tasks on corpus data. Hence, this approach focuses on 
the statistical/quantitative track of Natural Language Processing 
(NLP).</p>

<h2>Related fields</h2>

<ul>
<li>Computational Linguistics (CL)</li>
<li>Corpus Linguistics</li>
<li>Information Retrieval</li>
<li>Artificial Intelligence (AI), Machine Learning (ML) and Pattern 
Recognition</li>
</ul>

<p>The differences among the aforementioned fields related to NLP are a
matter of perspective and taste. Nonetheless, NLP is more frequently
regarded to be an engineering-oriented approach while CL is rather more
associated with theoretical aspects.</p>

<h2>Recommended bibliography</h2>

<h3>NLP specific</h3>

<ul>
<li>Speech and Language Processing - An Introduction to Natural Language 
Processing, Computational Linguistics, and Speech Recognition, by
Daniel Jurafsky and James H. Martin, 2009.</li>
<li>Introduction to Information Retrieval, by Christopher D. Manning, 
Prabhakar Raghavan and Hinrich Schütze, 2008.</li>
<li>Foundations of Statistical Natural Language Processing, by
Christopher D. Manning and Hinrich Schütze, 1999.</li>
</ul>

<h3>AI/ML specific</h3>

<ul>
<li>Artificial Intelligence: A Modern Approach, by Stuart Russell and 
Peter Norvig, 2010.</li>
<li>Pattern recognition and machine learning, by Christopher M. Bishop,
2006.</li>
<li>Pattern classification, by Richard O. Duda, Peter E. Hart and David 
G. Stork, 2001.</li>
</ul>

